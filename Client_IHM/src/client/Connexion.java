    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        user = jUserTextField.getText();
        password = jTextField1.getText();
        serveur = jTextField2.getText();
        String result;
        try {
            result = get(serveur+"/users-authtest", user, password);
        } catch (IOException e) {
            result = e.getMessage();
        }

        
        if (result.contains("401")) {
            if (jCheckBox1.isSelected()==true) {
               JSONObject obj = new JSONObject();
            obj.put("username", user);
            obj.put("password", password);

            String jsonText = obj.toString();
            try {
                result = postCompte(serveur+"/users", jsonText);
            } catch (IOException e) {
                result = e.getMessage();
            } 
            }
            else{
                System.out.println("Autentification fail");
                
                
            }
            
        }

        ListePartie l = new ListePartie(user, password,serveur);
        l.setVisible(true);
        l.pack();
        this.setVisible(false);

    }//GEN-LAST:event_jButton1ActionPerformed

